def crear_columnas_derivadas(df: pd.DataFrame, logs: list) -> pd.DataFrame:
    df = df.copy()
    columnas_creadas = []

    # 1. Identificar si es un cliente nuevo (nunca contactado antes)
    if "pdays" in df.columns:
        df["es_cliente_nuevo"] = (df["pdays"] == -1).astype(int)
        columnas_creadas.append("es_cliente_nuevo")

    # 2. Score de endeudamiento (si tiene hipoteca Y préstamo personal)
    if "housing" in df.columns and "loan" in df.columns:
        df["tiene_doble_prestamo"] = ((df["housing"] == 1) & (df["loan"] == 1)).astype(int)
        columnas_creadas.append("tiene_doble_prestamo")

    # 3. Duración en minutos (más fácil de interpretar que segundos)
    if "duration" in df.columns:
        df["duration_min"] = df["duration"] / 60
        columnas_creadas.append("duration_min")

    df["fecha_procesamiento"] = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    columnas_creadas.append("fecha_procesamiento")

    registrar_log(logs, "INFO", "Columnas derivadas", "TODAS",
                  f"Creadas: {columnas_creadas}", len(df), "Feature Engineering")
    return df

