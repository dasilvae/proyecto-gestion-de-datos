import numpy as np

def obtener_reglas_rango() -> dict:
    return {
        "age": (18, 100),
        "day": (1, 31),
        "campaign": (1, 50),
        "previous": (0, 300),
        "pdays": (-1, 999),
        "duration": (0, 5000),
        "balance": (-10000, 500000)
    }

def validar_rangos(df: pd.DataFrame, logs: list) -> pd.DataFrame:
    df = df.copy()
    reglas = obtener_reglas_rango()
    columnas_numericas = df.select_dtypes(include=[np.number]).columns.tolist()

    for col in columnas_numericas:
        valores_unicos = set(df[col].dropna().unique().tolist())
        if len(valores_unicos) > 0 and valores_unicos.issubset({0, 1}):
            reglas[col] = (0, 1)

    for col, (valor_min, valor_max) in reglas.items():
        if col not in df.columns: continue
        condicion_valida = df[col].between(valor_min, valor_max, inclusive="both")
        fuera_de_rango = (~condicion_valida).sum()
        if fuera_de_rango > 0:
            df = df.loc[condicion_valida].copy()
            registrar_log(logs, "ERROR", "Validación de rangos", col,
                          f"Valores fuera de [{valor_min}, {valor_max}]",
                          fuera_de_rango, "Eliminación de registros")
    return df

