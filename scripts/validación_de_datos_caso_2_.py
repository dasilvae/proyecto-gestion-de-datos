def eliminar_filas_vacias(df: pd.DataFrame, logs: list) -> pd.DataFrame:
    df = df.copy()
    filas_antes = len(df)
    df = df.dropna(how="all")
    filas_eliminadas = filas_antes - len(df)
    if filas_eliminadas > 0:
        registrar_log(logs, "WARNING", "Limpieza básica", "TODAS", "Filas vacías eliminadas", filas_eliminadas, "dropna")
    return df

def eliminar_duplicados(df: pd.DataFrame, logs: list) -> pd.DataFrame:
    df = df.copy()
    filas_antes = len(df)
    df = df.drop_duplicates()
    filas_eliminadas = filas_antes - len(df)
    if filas_eliminadas > 0:
        registrar_log(logs, "WARNING", "Limpieza básica", "TODAS", "Registros duplicados eliminados", filas_eliminadas, "drop_duplicates")
    return df

def eliminar_registros_con_nulos(df: pd.DataFrame, logs: list) -> pd.DataFrame:
    df = df.copy()
    filas_con_nulos = df.isna().any(axis=1)
    cantidad_nulos = filas_con_nulos.sum()
    df = df.loc[~filas_con_nulos].copy()
    if cantidad_nulos > 0:
        registrar_log(logs, "WARNING", "Limpieza básica", "TODAS", "Filas con nulos eliminadas", cantidad_nulos, "Eliminar nulos")
    return df

def limpieza_basica(df: pd.DataFrame, logs: list) -> pd.DataFrame:
    """
    Esta es la función que te falta en memoria.
    Ejecuta esta celda para definirla.
    """
    df = eliminar_filas_vacias(df, logs)
    df = eliminar_duplicados(df, logs)
    df = eliminar_registros_con_nulos(df, logs)
    return df

