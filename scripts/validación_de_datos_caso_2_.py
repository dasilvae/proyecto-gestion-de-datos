def estandarizar_nombres_columnas(df: pd.DataFrame, logs: list) -> pd.DataFrame:
    """
    Estandariza los nombres de las columnas del dataset.
    """
    df = df.copy()
    columnas_originales = df.columns.tolist()

    df.columns = (
        df.columns
        .astype(str)
        .str.strip()
        .str.lower()
        .str.replace(" ", "_", regex=False)
        .str.replace("-", "_", regex=False)
        .str.replace(".", "", regex=False)
    )

    columnas_nuevas = df.columns.tolist()

    registrar_log(
        logs=logs,
        nivel="INFO",
        etapa="Estandarización de columnas",
        columna="TODAS",
        descripcion=f"Columnas estandarizadas. Antes: {columnas_originales}. Después: {columnas_nuevas}.",
        filas_afectadas=0,
        accion="Normalización de nombres de columnas"
    )

    return df

# CELDA 7 Reglas de negocio#

def validar_semantica_bank(df: pd.DataFrame, logs: list) -> pd.DataFrame:
    # Regla: Duración no puede ser negativa
    duracion_negativa = df[df['duration'] < 0]
    if not duracion_negativa.empty:
        registrar_log(logs, "ERROR", "Semántica", "duration", "Duraciones negativas encontradas", len(duracion_negativa), "Revisar origen")

    # Regla: Si pdays es -1, previous debe ser 0
    inconsistencia_previos = df[(df['pdays'] == -1) & (df['previous'] != 0)]
    if not inconsistencia_previos.empty:
        registrar_log(logs, "WARNING", "Semántica", "pdays/previous", "Inconsistencia en historial previo", len(inconsistencia_previos), "Notificar")

    # Regla: Edad mínima 18 años
    menores_edad = df[df['age'] < 18]
    if not menores_edad.empty:
        registrar_log(logs, "ERROR", "Semántica", "age", "Clientes menores de 18 años", len(menores_edad), "Filtrar o revisar")

    return df

